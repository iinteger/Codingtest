# 책 풀이의 도움을 받음

n = int(input())

arr = [0]* 1001
arr[1] = 1
arr[2] = 3

# 한줄 전까지 다 차있으면 하나 추가 -> 경우의 수 변화 없음
# 두줄 전까지 다 차있으면 두가지 방법으로 채울 수 있어서 곱하기 2 (위 경우를 제외해야함)
# 세줄 전은 의미없음

for i in range(3, n+1):
    arr[i] = (arr[i-1] + arr[i-2]*2)
    # arr[i] = (arr[i-1] + arr[i-2]*2)%796796

print(arr[n] % 796796)